package ui;

public class ArrayPair {
    private int[] intArr;
    private String[] chArr;

    public ArrayPair(int[] intArr, String[] chArr) {
        this.intArr = intArr;
        this.chArr = chArr;
    }

    public int[] getIntArr() {
        return intArr;
    }

    public String[] getChArr() {
        return chArr;
    }
}
